function minutesBefore(min, max) {
  const mins = Math.floor(Math.random() * (max - min + 1)) + min;
  const d = new Date(Date.now() - mins * 60000);
  return `${d.getHours().toString().padStart(2, "0")}:${d.getMinutes().toString().padStart(2, "0")}`;
}

